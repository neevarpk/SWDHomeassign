package week2.day1;

public class FindOddNumbers {
	    public static void main(String[] args) {
	       
	        
	        for (int i = 1; i <= 10; i++) {
	  // Check if the remainder when dividing the number by 2 is equal to 1 (odd number)
	            if (i % 2 == 1) {
	                // Print the odd number
	                System.out.println(i);
	            }
	            	
	        }
	    }
	}
